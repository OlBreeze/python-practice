# Асинхронний таймаут
# Напишіть функцію slow_task(), яка іміт виконання завдання протягом 10 секунд.
# Використовуючи asyncio.wait_for(), викличте slow_task() з таймаутом 5 секунд.
# Якщо завдання не встигає виконатися за цей час, виведіть
# повідомлення про перевищення часу очікування.

import asyncio


async def slow_task() -> None:
    """
    Імітує повільне завдання, яке виконується 10 секунд.
    """
    print("Завдання почалося...")
    await asyncio.sleep(10)
    print("Завдання завершено!")


async def main() -> None:
    """
    Запускає slow_task() з таймаутом у 5 секунд.
    Якщо завдання не завершиться вчасно — обробляє TimeoutError.
    """
    try:
        await asyncio.wait_for(slow_task(), timeout=5)
    except asyncio.TimeoutError:
        print("⏰ Перевищено час очікування (таймаут 5 секунд)!")


# --------------------------------------------------
if __name__ == "__main__":
    asyncio.run(main())
